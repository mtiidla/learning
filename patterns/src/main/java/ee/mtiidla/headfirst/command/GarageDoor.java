package ee.mtiidla.headfirst.command;

class GarageDoor {

    void up() {
        System.out.println("Garage door sliding up");
    }

    void down() {
        System.out.println("Garage door sliding down");
    }

    void stop() {
        System.out.println("Garage door stopped");
    }

    void lightOn() {
        System.out.println("Garage light on");
    }

    void lightOff() {
        System.out.println("Garage light off");
    }

}
